package model;

public class Item {
	private Integer item_seqno,item_price,item_stock,item_state;
	private String item_name,item_writer,item_content,item_image;
	public Integer getItem_seqno() {
		return item_seqno;
	}
	public void setItem_seqno(Integer item_seqno) {
		this.item_seqno = item_seqno;
	}
	public Integer getItem_price() {
		return item_price;
	}
	public void setItem_price(Integer item_price) {
		this.item_price = item_price;
	}
	public Integer getItem_stock() {
		return item_stock;
	}
	public void setItem_stock(Integer item_stock) {
		this.item_stock = item_stock;
	}
	public Integer getItem_state() {
		return item_state;
	}
	public void setItem_state(Integer item_state) {
		this.item_state = item_state;
	}
	public String getItem_name() {
		return item_name;
	}
	public void setItem_name(String item_name) {
		this.item_name = item_name;
	}
	public String getItem_writer() {
		return item_writer;
	}
	public void setItem_writer(String item_writer) {
		this.item_writer = item_writer;
	}
	public String getItem_content() {
		return item_content;
	}
	public void setItem_content(String item_content) {
		this.item_content = item_content;
	}
	public String getItem_image() {
		return item_image;
	}
	public void setItem_image(String item_image) {
		this.item_image = item_image;
	}
	
	
}
